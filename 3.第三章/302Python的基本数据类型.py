# 整数: int
# 浮点型: float
# 其它语言: 单精度(float), 双精度(double)
# 其它语言: short, int, long

# Number数字：整数int 浮点数float
print(type(1+0.1))  # float

print(type(1+1.0))  # float

print(type(1*1))    # int
print(type(1*1.0))  # float

print(type(2/2))    # float
print(type(2//2))   # int